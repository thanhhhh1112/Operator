package thanh.dev;

public class MainApp { 
	public static void main(String[] args) {
		int a = 9;
		int b = 5;
		// Phep cong
		int tong = a + b;
		System.out.println("Tong: " + tong);
		// Phep tru
		int hieu = a - b;
		System.out.println("Hieu: " + hieu);
		// Phep nhan
		int tich = a * b;
		System.out.println("Tich: " + tich);
		// Phep chia
		int thuong = a / b;
		System.out.println("Thuong: " + thuong);
		
	
		
	}

}
